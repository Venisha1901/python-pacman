def count_remaining_dots():
    """Count the remaining dots and power pellets in the maze"""
    dot_count = 0
    for row in level:
        dot_count += row.count(1) + row.count(2)
    return dot_count


def find_nearest_dot(center_x, center_y):
    """Find the nearest dot or power pellet in the maze"""
    num1 = (HEIGHT - 50) // 32
    num2 = WIDTH // 30
    
    # Convert player position to grid coordinates
    grid_x = center_x // num2
    grid_y = center_y // num1
    
    # Search for nearest dot and power pellet
    min_dot_distance = float('inf')
    min_power_distance = float('inf')
    nearest_dot_pos = None
    nearest_power_pos = None
    
    # Search in a reasonable radius first (optimization)
    search_radius = 10
    
    # Define search boundaries
    min_y = max(0, grid_y - search_radius)
    max_y = min(len(level), grid_y + search_radius)
    
    for y in range(min_y, max_y):
        min_x = max(0, grid_x - search_radius)
        max_x = min(len(level[0]), grid_x + search_radius)
        
        for x in range(min_x, max_x):
            # Calculate Manhattan distance
            distance = abs(grid_x - x) + abs(grid_y - y)
            
            # Check for regular dot
            if level[y][x] == 1:
                if distance < min_dot_distance:
                    min_dot_distance = distance
                    nearest_dot_pos = (x, y)
            
            # Check for power pellet
            elif level[y][x] == 2:
                if distance < min_power_distance:
                    min_power_distance = distance
                    nearest_power_pos = (x, y)
    
    # If we didn't find anything in the radius, search the entire maze
    if nearest_dot_pos is None and nearest_power_pos is None:
        for y in range(len(level)):
            for x in range(len(level[0])):
                # Calculate Manhattan distance
                distance = abs(grid_x - x) + abs(grid_y - y)
                
                # Check for regular dot
                if level[y][x] == 1:
                    if distance < min_dot_distance:
                        min_dot_distance = distance
                        nearest_dot_pos = (x, y)
                
                # Check for power pellet
                elif level[y][x] == 2:
                    if distance < min_power_distance:
                        min_power_distance = distance
                        nearest_power_pos = (x, y)
    
    # Prioritize power pellet if it's close enough
    if nearest_power_pos is not None and min_power_distance < min_dot_distance * 1.5:
        return nearest_power_pos
    
    return nearest_dot_pos if nearest_dot_pos is not None else nearest_power_pos


def calculate_ghost_threats():
    """Calculate threat level for each ghost based on distance and state"""
    ghost_threats = []
    
    # Get ghost positions
    ghost_positions = [
        (blinky_x, blinky_y, blinky_dead, 0),
        (inky_x, inky_y, inky_dead, 1),
        (pinky_x, pinky_y, pinky_dead, 2),
        (clyde_x, clyde_y, clyde_dead, 3)
    ]
    
    for ghost_x, ghost_y, is_dead, ghost_id in ghost_positions:
        # Skip dead ghosts
        if is_dead:
            continue
        
        # Calculate Euclidean distance
        distance = ((ghost_x - player_x) ** 2 + (ghost_y - player_y) ** 2) ** 0.5
        
        # Calculate threat level (higher for closer ghosts)
        threat = 0
        if not is_dead and not (powerup and not eaten_ghost[ghost_id]):
            if distance < 80:  # Very close
                threat = 10
            elif distance < 150:  # Close
                threat = 7
            elif distance < 250:  # Medium distance
                threat = 4
            elif distance < 350:  # Moderate distance
                threat = 2
            else:  # Far
                threat = 1
            
            # Adjust threat based on ghost type
            if ghost_id == 0:  # Blinky - aggressive
                threat *= 1.5
            elif ghost_id == 2:  # Pinky - tries to ambush
                threat *= 1.3
        
        ghost_threats.append((distance, threat, is_dead, ghost_id))
    
    return ghost_threats


def find_safe_direction(turns, ghost_threats):
    """Find a safe direction to move based on ghost threats"""
    # Directions: 0: Right, 1: Left, 2: Up, 3: Down
    
    # If no valid turns, keep current direction
    if not any(turns):
        return direction
    
    # Calculate direction scores
    direction_scores = [0, 0, 0, 0]
    
    # First, apply the turns_allowed constraint
    for i in range(4):
        if not turns[i]:
            direction_scores[i] = -1000  # Cannot go this way
    
    # Get player grid position
    num1 = (HEIGHT - 50) // 32
    num2 = WIDTH // 30
    grid_x = (player_x + 23) // num2
    grid_y = (player_y + 24) // num1
    
    # For each ghost, calculate avoidance scores
    for distance, threat, is_dead, ghost_id in ghost_threats:
        if is_dead or (powerup and not eaten_ghost[ghost_id]):
            continue  # Skip dead ghosts or ghosts we can eat
        
        ghost_grid_x = (eval(f"{['blinky', 'inky', 'pinky', 'clyde'][ghost_id]}_x") + 22) // num2
        ghost_grid_y = (eval(f"{['blinky', 'inky', 'pinky', 'clyde'][ghost_id]}_y") + 22) // num1
        
        # Calculate direction to ghost
        dx = ghost_grid_x - grid_x
        dy = ghost_grid_y - grid_y
        
        # Penalize directions that move toward ghosts
        if dx > 0 and turns[0]:  # Ghost is to the right
            direction_scores[0] -= threat * 10
        if dx < 0 and turns[1]:  # Ghost is to the left
            direction_scores[1] -= threat * 10
        if dy < 0 and turns[2]:  # Ghost is above
            direction_scores[2] -= threat * 10
        if dy > 0 and turns[3]:  # Ghost is below
            direction_scores[3] -= threat * 10
    
    # Avoid reversing direction unless necessary
    opposite_dir = {0: 1, 1: 0, 2: 3, 3: 2}
    valid_directions = [i for i in range(4) if turns[i] and direction_scores[i] > -1000]
    
    if len(valid_directions) > 1 and opposite_dir[direction] in valid_directions:
        direction_scores[opposite_dir[direction]] -= 50
    
    # Choose direction with highest score
    best_directions = [i for i in range(4) if turns[i] and direction_scores[i] > -1000]
    
    if best_directions:
        return max(best_directions, key=lambda d: direction_scores[d])
    
    # If all directions are bad, just pick any valid one
    for i in range(4):
        if turns[i]:
            return i
    
    return direction  # Fallback to current direction


